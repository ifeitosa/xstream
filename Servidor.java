package analisador2.dumb;

public class Servidor {

	private Integer inteiroServidor;
	private Boolean booleanoServidor;
	private String stringServidor;
	
	public Servidor() {
		
	}

	public Integer getInteiroServidor() {
		return inteiroServidor;
	}

	public void setInteiroServidor(Integer inteiroServidor) {
		this.inteiroServidor = inteiroServidor;
	}

	public Boolean getBooleanoServidor() {
		return booleanoServidor;
	}

	public void setBooleanoServidor(Boolean booleanoServidor) {
		this.booleanoServidor = booleanoServidor;
	}

	public String getStringServidor() {
		return stringServidor;
	}

	public void setStringServidor(String stringServidor) {
		this.stringServidor = stringServidor;
	}
	
	
}
